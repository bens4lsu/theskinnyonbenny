	  <h2>Ben's secret recipe for red beans</h2>
	  <p> Soak a small pack of red beans, starting the night before.<br>
          <br>
	    The day of the cooking, sautee a full bunch of celery, six bell peppers, and six large onions, all chopped fine.  It seems like too much, espeially when you're well into your second hour of chopping vegatables, and your pot is full to the brim, but get out a bigger pot, and stick to the program.  It doesn't matter what fat you use to sautee (vegatable oil, olive oil, butter).<br>
  <br>
	    Once all the vegetables are soft, dump the bloated swollen beans into the pot.  Don't use the pink water that the beans were soaking in, as this water contains much of the bean by-product that makes one fart.  I've never seen this proven scientifically, but I have much personal evidence that this is factual.<br>
  <br>
	    I like to add a shitload of wine at this point.  I've never measured it out, but I would guess 2-3 cups.  I ususally find whatever bottle that is half-consumed, and dump the rest of it in the pot.  Then, add a healthy squeeze of lemon juice (maybe 1/4 cup or a little less), and fill the pot with water so that the water is about 4 inches above the solids.<br>
  <br>
	    Then, I season it.  Again, there are no measurements or fixed list of ingredients.  I just go through every spice in the cabinet and ask myself, &quot;would that be good?&quot;  Things like cayenne, basil, oregano, and bay leaves are good.  Ginger, numeg, cinnamon, not so much.<br>
  <br>
	    Add a little at a time, taste, add a little more.  Leave it a bit less salty than you like it.  It gets a lot saltier as the water boils away.  This is my achilles heel of cooking beans.  If you're going to add liquid smoke or browning sauce, it will taste very good, but go kind of easy.  Not more than a couple of tablespoons.<br>
  <br>
	    Get all of this simmering, cover and find something else to do.  Check on it now and then to make sure nothing sticks to the bottom of the pot, but do nothing else for a couple of hours.<br>
  <br>
	    After that, add sausage.  Important note:  &lt;i&gt;there is no such thing as too much sausage!&lt;/i&gt;  Put at least a pound.  Two is better.  Three is better still.<br>
  <br>
	    Cook for at least two more hours.  The longer this simmers, the better it tastes.  I don't know why, but that's the fact of it.  It will be even better if you just let it sit on the stove top, covered, with the stove off all night.<br>
  <br>
	    Enjoy.<br>
  <br>
	    And remember, go easy on the salt.</p>
