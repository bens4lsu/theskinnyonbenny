			<h2>Being white and southern in New Jersey, Part 1</h2>
			<small>August 27, 2006</small>


			<div class="entrytext">Since my apartment will not be ready until September 1, Seton Hall is housing me in an apartment on their main campus.  I was driving around on Saturday and saw this very pretty United Methodist Church.  Small Church with lots of pretty stain glass window.  I thought I would give it a try -- you know, start the school year off on the right foot.  The sign said services at 10:00 on Sunday.  Went to the Sunday service - - - ONLY WHITE PERSON IN THE WHOLE PLACE.   Come to find out, it is a mainly African-Carrribean congregation.  The sermon was entitled "When Trouble Comes."   You are not going to believe this but a good part of the sermon was about not turning to witchcraft when trouble comes but to turn to God.  I almost fell off the pew.  I was absolutely amazed.   I could not tell you how many "Amens" "Praise God" and "Thank You Jesus'" were volunteered.  To top it all off, the service lasted an hour and forty five minutes.  I was so entranced I didn't even notice until I got in my car.  It was quite an experience. You would think that a UMC would be pretty generic and vanilla.  BOY WAS I WRONG!!!  </div>
